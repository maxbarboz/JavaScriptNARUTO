// operadores relacionais
// == -> igual a
// != -> diferente de

var x=10;
var y=20;

if(x!=y){
    console.log(x!=y)
    console.log(x)
}else{
    console.log(x!=y)
    console.log(y)
}

if(x==y){
    console.log(x==y)
    console.log(x)
}else{
    console.log(x==y)
    console.log(y)
}

// operadores de igualdade de dados
// === -> igual a, e do mesmo tipo
// !== -> diferente, e do mesmo tipo


console.log(x!==y)

console.log(x===y)


