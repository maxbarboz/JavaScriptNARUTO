var x = 10, y = 2;

var x = x*y

console.log(x)

/* operadores:

adição = +
subtração = -
divisão = /
multiplicação = *

*/

console.log(x+y)
console.log(x-y)
console.log(x/y)
console.log(x*y)

/* operadores abreviados:
++ = soma;
-- = sub;
*/

console.log(x++)
console.log(x--)

// com o sinais de soma/sub pós variável o valor já atualiza

x=20
console.log(++x)
console.log(--x)

// outros formatos de abreviadores

console.log(x+=10)
console.log(x-=10)


